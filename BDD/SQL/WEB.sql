CREATE TABLE ANUNCIOS (

	CODIGO NUMBER (5) NOT NULL,

	NOMBRE VARCHAR2(20) NOT NULL,

	TITULO VARCHAR2 (30) NOT NULL,

	CONTENIDO  VARCHAR2(20) NOT NULL,

	CATEGORIA VARCHAR (1) NOT NULL,

	PRECIO	NUMBER (4,2) NOT NULL,

	CODIGO_TIPOANUNCIO	VARCHAR (1) NOT NULL,

	DNI_CLIENTES NUMBER (9) NOT NULL,

	CONSTRAINT PK_ANUNCIOS PRIMARY KEY (CODIGO),

	CONSTRAINT FK_TIPOSANUNCIOS FOREIGN KEY (CODIGO_TIPOSANUNCIOS),

	CONSTRAINT FK_CLIENTES	FOREIGN KEY (DNI_CLIENTES)
	);

CREATE TABLE CLIENTES (

	DNI NUMBER(9) NOT NULL,

	DIRECCION	VARCHAR2 (30) NOT NULL,

	POBLACION	VARCHAR2 (20) NOT NULL,

	PROVINCIA	VARCHAR2 (20) NOT NULL,

	CODIGO_POSTAL	VARCHAR2 (5) NOT NULL,

	EMAIL	VARCHAR2 (30) NOT NULL,
	CONSTRAINT PK_CLIENTES PRIMARY KEY (DNI)
	);

CREATE TABLE TIPOSANUNCIOS (

	NOMBRE	VARCHAR2 (25) NOT NULL, 

	CODIGO	NUMBER (5) NOT NULL,

	CONSTRAINT PK_TIPOSANUNCIOS (CODIGO) NOT NULL,
	);

CREATE TABLE WEBS (

	URL	VARCHAR2 (35) NOT NULL,

	NOMBRE  VARCHAR2(30) NOT NULL,

	TOPICO	VARCHAR (1) NOT NULL,

	CONSTRAINT PK_WEBS PRIMARY KEY (URL),
	);

CREATE TABLE APARECE (

	FECH_INICIO	DATE NOT NULL,

	FECH_FIN 	DATE,

	URL_WEBS	VARCHAR2 (35) NOT NULL,

	CODIGO_ANUNCIOS	NUMBER (5) NOT NULL,                                                                                                                                                                   

	CONSTRAINT PK_APARECE PRIMARY KEY (FECH_INICIO,URL_WEBS,CODIGO_ANUNCIOS),

	CONSTRAINT FK_WEBS FOREIGN KEY (URL_WEBS),

	CONSTRAINT FK_ANUNCIOS FOREIGN KEY (CODIGO_ANUNCIOS)
	);